#include<iostream>

using namespace std;

int main()
{
    cout<<"memory of int: "<<sizeof(int)<<endl;
    cout<<"memory of bool: "<<sizeof(bool)<<endl;
    cout<<"memory of char: "<<sizeof(char)<<endl;
    cout<<"memory of double: "<<sizeof(double)<<endl;
    cout<<"memory of float: "<<sizeof(float)<<endl;
    return 0;
}